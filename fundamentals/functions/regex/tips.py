# quantifiers
# []?  - occurs 0 or 1 time
# []+  - occurs 1 or more times
# []*  - occurs - s 0 or more times
# []{n}  - occurs n times
# []{n,}  - occurs n or more times
# []{y,z}  - occurs at least y times but less than z times
#
